CREATE TABLE TOURISTS (
TOURIST_ID VARCHAR2(10) PRIMARY KEY,
TOURIST_NAME VARCHAR2(50) NOT NULL,
ADDRESS VARCHAR2(100),
DATE_OF_BIRTH DATE,
PHONE VARCHAR2(15)
);
CREATE TABLE RESERVATION (
RESERVATION_ID VARCHAR2(10) PRIMARY KEY,
TOURIST_ID VARCHAR2(10),
BOAT_ID VARCHAR2(10),
SAILOR_ID VARCHAR2(10),
RESERVATION_DATE DATE,
SAIL_DATE DATE,
NUMBER_OF_PEOPLE NUMBER(3),

FOREIGN KEY (TOURIST_ID) REFERENCES TOURISTS(TOURIST_ID),
FOREIGN KEY (BOAT_ID) REFERENCES BOATS(BOAT_ID),
FOREIGN KEY (SAILOR_ID) REFERENCES SAILORS(SAILOR_ID)
);
CREATE TABLE PAYMENT (
PAYMENT_ID VARCHAR2(10) PRIMARY KEY,
RESERVATION_ID VARCHAR2(10),
PAYMENT_AMOUNT NUMBER(10,2),
PAYMENT_DATE DATE,
PAYMENT_MODE VARCHAR2(20),
PAYMENT_STATUS VARCHAR2(20),

FOREIGN KEY (RESERVATION_ID) REFERENCES RESERVATION(RESERVATION_ID)
);
