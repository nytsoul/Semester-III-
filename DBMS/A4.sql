SELECT T.TOURIST_ID, T.TOURIST_NAME, R.RESERVATION_ID
FROM TOURISTS T
JOIN RESERVATION R ON T.TOURIST_ID = R.TOURIST_ID
JOIN BOATS B ON R.BOAT_ID = B.BOAT_ID
WHERE B.BOAT_TYPE IN ('LUX','CAR','CRU')
GROUP BY T.TOURIST_ID, T.TOURIST_NAME, R.RESERVATION_ID
HAVING COUNT(DISTINCT B.BOAT_TYPE) = 3;

