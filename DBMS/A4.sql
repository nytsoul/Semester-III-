SELECT S.RATING, AVG(P.PAYMENT_AMOUNT)
FROM SAILORS S
JOIN RESERVATION R ON S.SAILOR_ID = R.SAILOR_ID
JOIN PAYMENT P ON R.RESERVATION_ID = P.RESERVATION_ID
WHERE P.PAYMENT_STATUS = 'PAID'
GROUP BY S.RATING;

SELECT B.BOAT_NAME, SUM(P.PAYMENT_AMOUNT)
FROM BOATS B
JOIN RESERVATION R ON B.BOAT_ID = R.BOAT_ID
JOIN PAYMENT P ON R.RESERVATION_ID = P.RESERVATION_ID
WHERE R.TOURIST_ID = 'T001'
AND P.PAYMENT_STATUS = 'PAID'
GROUP BY B.BOAT_NAME
HAVING SUM(P.PAYMENT_AMOUNT) > (
SELECT MAX(PAYMENT_AMOUNT)
FROM PAYMENT
);

